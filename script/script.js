'use strict';

var n = '23';

switch (n) {
    case 'one':
        console.log('Первый CASE');
        break;
    case 'two':
        console.log('Второй CASE');
        break;
    case 'three':
        console.log('Третий CASE');
        break;
    default:
        alert('Ни первый ни второй ни третий CASE не срабтал, увы и ах');
}
